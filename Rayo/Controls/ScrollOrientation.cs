namespace Rayo.Controls;

/// <summary>
/// Enumeration specifying the direction of scrolling for a ScrollView.
/// </summary>
public enum ScrollOrientation
{
    /// <summary>
    /// Vertical scrolling only.
    /// </summary>
    Vertical,

    /// <summary>
    /// Horizontal scrolling only.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Both vertical and horizontal scrolling.
    /// </summary>
    Both,

    /// <summary>
    /// No scrolling.
    /// </summary>
    Neither
}
